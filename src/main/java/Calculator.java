public class Calculator {
    public double sum(double x, double y) {
        return x + y;
    }

    public double subtract(double x, double y) {
        return x - y;
    }

    public double divide(double x, double y) {
        return x / y;
    }

    public double mulitply(double x, double y) {
        return x * y;
    }
}
